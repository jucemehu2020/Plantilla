    private void btnRegistrarClienteActionPerformed(java.awt.event.ActionEvent evt) throws Exception {//GEN-FIRST:event_btnRegistrarClienteActionPerformed
        CreateNewClient createNewClient = new CreateNewClient();
        
        if (txtRegistroNombreCompleto.getText().isEmpty() || txtRegistroApellidoCompleto.getText().isEmpty() || txtFechaNacimiento.getText().isEmpty() || 
                txtRegistroCorreoCliente.getText().isEmpty() || txtRegistroContrasenaCliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ningun campo puede estar vacio");
        }else{
            int dialogo = JOptionPane.CLOSED_OPTION;
            int aux = 0;
            String nombreCliente = txtRegistroNombreCompleto.getText();
            String apellidoCliente = txtRegistroApellidoCompleto.getText();
            String fechaNacCliente = txtFechaNacimiento.getText();
            String correoCliente = txtRegistroCorreoCliente.getText();
            String contrasenaCliente = txtRegistroContrasenaCliente.getText();
            createNewClient.clientRegistry(nombreCliente, apellidoCliente, fechaNacCliente, correoCliente, contrasenaCliente);
            JOptionPane.showConfirmDialog(null, "°Se ha registrado con Exito!");

        }
        
        
        
    }//GEN-LAST:event_btnRegistrarClienteActionPerformed

